# 6. BOT SETUP
# =============================================================================

# Required intents:
#   - message_content: to read and log user messages
#   - members:         to inspect user roles for the PsycheOptIn gate
intents = discord.Intents.default()
intents.message_content = True
intents.members = True

class PsycheBot(commands.Bot):
    """
    Custom Bot subclass. Using setup_hook() to initialize async resources
    (heartbeat server, DB) before the bot's connection is established.
    """

    async def setup_hook(self):
        """Called by discord.py after login but before connecting to the Gateway."""
        # Start the heartbeat server so HF Spaces sees port 7860 immediately
        await start_heartbeat()
        log.info("setup_hook complete.")

    async def on_ready(self):
        """Fired once the bot is fully connected and ready to receive events."""
        # Initialize the database (idempotent — safe to call on reconnect)
        init_db()

        # Set a Rich Presence status
        activity = discord.Activity(
            type=discord.ActivityType.watching,
            name="behavioral patterns | !help"
        )
        await self.change_presence(status=discord.Status.online, activity=activity)

        log.info("=" * 50)
        log.info("✨ Psyche v2 Online")
        log.info("   Logged in as : %s (ID: %s)", self.user, self.user.id)
        log.info("   Model        : %s", MODEL_ID)
        log.info("   Servers      : %d", len(self.guilds))
        log.info("   Owner ID     : %s", OWNER_ID or "NOT SET")
        log.info("=" * 50)


bot = PsycheBot(
    command_prefix='!',
    intents=intents,
    help_command=None   # We implement our own !help embed in Phase 5
)

