# 4. DATABASE INITIALIZATION
# =============================================================================

def init_db():
    """
    Creates the SQLite database and all required tables if they don't exist.
    Called once during bot startup via on_ready.
    """
    # Ensure the /data directory exists (important for local dev)
    os.makedirs(os.path.dirname(DB_PATH), exist_ok=True)

    conn = sqlite3.connect(DB_PATH)
    cursor = conn.cursor()

    # -- Message History Table --
    # Stores opted-in user messages for behavioral analysis.
    cursor.execute('''
        CREATE TABLE IF NOT EXISTS messages (
            id        INTEGER PRIMARY KEY AUTOINCREMENT,
            user_id   TEXT NOT NULL,
            guild_id  TEXT NOT NULL,
            content   TEXT NOT NULL,
            timestamp DATETIME DEFAULT CURRENT_TIMESTAMP
        )
    ''')

    # -- Quiz Results Table --
    # Stores completed personality quiz results per user per server.
    cursor.execute('''
        CREATE TABLE IF NOT EXISTS quiz_results (
            user_id      TEXT NOT NULL,
            guild_id     TEXT NOT NULL,
            quiz_type    TEXT NOT NULL,
            result_summary TEXT,
            raw_answers  TEXT,
            completed_at DATETIME,
            PRIMARY KEY (user_id, guild_id, quiz_type)
        )
    ''')

    # -- Active Quiz Sessions Table --
    # Allows users to pause and resume in-progress quizzes.
    cursor.execute('''
        CREATE TABLE IF NOT EXISTS quiz_sessions (
            user_id          TEXT PRIMARY KEY,
            quiz_type        TEXT NOT NULL,
            current_question INTEGER DEFAULT 0,
            answers          TEXT DEFAULT '[]',
            started_at       DATETIME DEFAULT CURRENT_TIMESTAMP
        )
    ''')

    # -- Cooldowns Table --
    # Tracks the last time a user ran a rate-limited command.
    cursor.execute('''
        CREATE TABLE IF NOT EXISTS cooldowns (
            user_id   TEXT NOT NULL,
            command   TEXT NOT NULL,
            last_used DATETIME NOT NULL,
            PRIMARY KEY (user_id, command)
        )
    ''')

    conn.commit()
    conn.close()
    log.info("Database initialized at %s", DB_PATH)

# =============================================================================
# 5. HEARTBEAT WEB SERVER (aiohttp — native async, no threading)
# =============================================================================

async def heartbeat_handler(request):
    """
    Responds to HTTP GET / with a plain-text status message.
    Hugging Face checks this endpoint to confirm the Space is 'Running'.
    """
    return web.Response(text="Psyche is Awake")

async def start_heartbeat():
    """
    Starts the aiohttp web server on 0.0.0.0:7860.
    Runs natively in the event loop — no threads, no blocking.
    Called from PsycheBot.setup_hook() so it starts before the bot connects.
    """
    web_app = web.Application()
    web_app.router.add_get('/', heartbeat_handler)

    runner = web.AppRunner(web_app)
    await runner.setup()

    site = web.TCPSite(runner, host='0.0.0.0', port=7860)
    await site.start()

    log.info("Heartbeat server listening on http://0.0.0.0:7860")

